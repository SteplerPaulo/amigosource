	function getBusinessContactInfo($business_id,$prefix=null){
			$this->tablePrefix = $prefix;
			$this->User->tablePrefix = $prefix;
			$this->recursive = 0;
			$business = $this->find('first',array('conditions'=>array('Business.id'=>$business_id),'fields'=>array('Business.contact_name','Business.user_id')));
			$user = $this->User->find('first',array('conditions'=>array('User.id'=>$business['Business']['user_id']),'fields'=>array('User.email')));
			$business['User'] = $user['User'];
			return $business;
	}
